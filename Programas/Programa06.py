
# Escribir un programa en el cual se ingresen cuatro números,
# calcular e informar la suma de los dos primeros y el producto del tercero y el cuarto.

numero1=int(input("Ingrese el primer número: "))
numero2=int(input("Ingrese el segundo número: "))
numero3=int(input("Ingrese el tercer número: "))
numero4=int(input("Ingrese el cuarto número: "))

primerosNumeros=numero1+numero2
ultimosNumeros=numero3*numero4

print("La suma de los dos primeros números es: ", primerosNumeros)
print("El producto de los dos últimos números es: ", ultimosNumeros)
